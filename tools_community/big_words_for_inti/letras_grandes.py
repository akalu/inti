DESCONOCIDA = [
    " ##### ",
    "#     #",
    "#  #  #",
    "#  #  #",
    "#     #",
    "#  #  #",
    " ##### ",
]


def dibujar_texto(texto):
    texto = texto.upper()
    filas = [""] * 7
    for i, char in enumerate(texto):
        patron = LETRAS.get(char, DESCONOCIDA)
        for fila_idx in range(7):
            filas[fila_idx] += patron[fila_idx] + "  "
    for fila in filas:
        print(fila)


def dibujar_cuadrado():
    """Cuadrado pequeño sólido 5x5"""
    lineas = [
        "#####",
        "#####",
        "#####",
        "#####",
        "#####",
    ]
    for l in lineas:
        print(l)


def dibujar_triangulo():
    """Triángulo pequeño apuntando hacia arriba"""
    lineas = [
        "  #  ",
        " ### ",
        "#####",
        "#####",
        "#####",
    ]
    for l in lineas:
        print(l)


def dibujar_circulo():
    """Círculo pequeño hueco 5x5"""
    lineas = [
        " ### ",
        "#   #",
        "#   #",
        "#   #",
        " ### ",
    ]
    for l in lineas:
        print(l)


def dibujar_separador():
    print()
    print("  ·  ·  ·")
    print()


def main():
    if len(sys.argv) < 2:
        texto = input("Ingresá la palabra u oración: ")
    else:
        texto = " ".join(sys.argv[1:])

    if not texto.strip():
        print("No ingresaste nada.")
        return

    print()
    print("=" * 60)
    dibujar_texto(texto)
    print("=" * 60)
    print()
    print("  [ cuadrado ]     [ triángulo ]     [ círculo ]")
    print()

    # Dibujar las tres formas en paralelo (misma cantidad de líneas)
    cuadrado  = ["#####", "#####", "#####", "#####", "#####"]
    triangulo = ["  #  ", " ### ", "#####", "#####", "#####"]
    circulo   = [" ### ", "#   #", "#   #", "#   #", " ### "]

    for c, t, o in zip(cuadrado, triangulo, circulo):
        print(f"    {c}          {t}          {o}")

    print()


if __name__ == "__main__":
    main()
